package com.taobao.guangjie.route;

public class JSONPoint {

	public int id;
	public String shopPos;
	public int level;
	public int x, y;

	public JSONPoint(int id, String shopPos, int level, int x, int y) {
		this.id = id;
		this.shopPos = shopPos;
		this.level = level;
		this.x = x;
		this.y = y;
	}

}
